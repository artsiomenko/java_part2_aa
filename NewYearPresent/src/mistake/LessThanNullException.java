package mistake;

@SuppressWarnings("serial")
public class LessThanNullException extends Exception {

	public void Exception() {
		
	}

	public LessThanNullException(Throwable cause) {
		super(cause);
		
	}

	public LessThanNullException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public LessThanNullException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public LessThanNullException(String message) {
		super(message);
	}

}
